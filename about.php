    <h1 class="uk-text-center uk-margin-xlarge-top uk-text-primary">What is Unix TimeStamp?</h1>
    <br>
    <p class="uk-text-center uk-margin-xlarge-left uk-margin-xlarge-right uk-text-primary">The unix time stamp is a way to track time as a running total of seconds. This count starts at the Unix Epoch on January 1st, 1970 at UTC. Therefore, the unix time stamp is merely the number of seconds between a particular date and the Unix Epoch. It should also be pointed out (thanks to the comments from visitors to this site) that this point in time technically does not change no matter where you are located on the globe. This is very useful to computer systems for tracking and sorting dated information in dynamic and distributed applications both online and client side.</p>
    
